# prints string text followed by calculated value (PEMDAS)
puts "Hens #{25.0 + 30 / 6}"
# prints sting text followed by calculated value (PEMDAS); % 4 same as * .04
puts "Roosters #{100.0 - 25 * 3 % 4}"

