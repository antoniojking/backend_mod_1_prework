#prints string as text; no calculations
puts "Is it true that 3 + 2 < 5 - 7?"

#prints calculated value (PEMDAS)
puts 3 + 2 < 5 - 7

# prints string as text followed by calculated value
puts "What is 3 + 2? #{3 + 2}"
# prints string as text followed by calculated value
puts "What is 5 - 7? #{5 - 7}"

